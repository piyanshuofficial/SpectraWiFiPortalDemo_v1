// Get aggregated stats for a segment (all sites combined)
export const getGuestStatsBySegment = (segment) => {
  const segmentSiteIds = Object.keys(guestStatistics).filter(siteId => {
    const guests = guestUsers.filter(g => g.siteId === siteId);
    return guests.length > 0 && guests[0].segment === segment;
  });

  if (segmentSiteIds.length === 0) {
    return {
      today: { totalGuests: 0, activeGuests: 0, checkedIn: 0, checkedOut: 0, pending: 0, dataUsed: '0 GB' },
      thisWeek: { totalGuests: 0, uniqueVisitors: 0, avgDuration: '0h', peakDay: '-', dataUsed: '0 GB' },
      thisMonth: { totalGuests: 0, uniqueVisitors: 0, avgDuration: '0h', topGuestType: '-', dataUsed: '0 GB', vouchersGenerated: 0, vouchersRedeemed: 0 },
      byType: {},
    };
  }

  // Aggregate stats from all sites
  const aggregated = {
    today: { totalGuests: 0, activeGuests: 0, checkedIn: 0, checkedOut: 0, pending: 0, dataUsed: 0 },
    thisWeek: { totalGuests: 0, uniqueVisitors: 0, avgDuration: 0, peakDay: '-', dataUsed: 0 },
    thisMonth: { totalGuests: 0, uniqueVisitors: 0, avgDuration: 0, topGuestType: '-', dataUsed: 0, vouchersGenerated: 0, vouchersRedeemed: 0 },
    byType: {},
  };

  segmentSiteIds.forEach(siteId => {
    const stats = guestStatistics[siteId];
    if (stats) {
      // Today
      aggregated.today.totalGuests += stats.today?.totalGuests || 0;
      aggregated.today.activeGuests += stats.today?.activeGuests || 0;
      aggregated.today.checkedIn += stats.today?.checkedIn || 0;
      aggregated.today.checkedOut += stats.today?.checkedOut || 0;
      aggregated.today.pending += stats.today?.pending || 0;
      aggregated.today.dataUsed += parseFloat(stats.today?.dataUsed) || 0;

      // This Week
      aggregated.thisWeek.totalGuests += stats.thisWeek?.totalGuests || 0;
      aggregated.thisWeek.uniqueVisitors += stats.thisWeek?.uniqueVisitors || 0;
      aggregated.thisWeek.dataUsed += parseFloat(stats.thisWeek?.dataUsed) || 0;

      // This Month
      aggregated.thisMonth.totalGuests += stats.thisMonth?.totalGuests || 0;
      aggregated.thisMonth.uniqueVisitors += stats.thisMonth?.uniqueVisitors || 0;
      aggregated.thisMonth.dataUsed += parseFloat(stats.thisMonth?.dataUsed) || 0;
      aggregated.thisMonth.vouchersGenerated += stats.thisMonth?.vouchersGenerated || 0;
      aggregated.thisMonth.vouchersRedeemed += stats.thisMonth?.vouchersRedeemed || 0;

      // By Type
      if (stats.byType) {
        Object.entries(stats.byType).forEach(([type, count]) => {
          aggregated.byType[type] = (aggregated.byType[type] || 0) + count;
        });
      }
    }
  });

  // Format data used values
  aggregated.today.dataUsed = `${aggregated.today.dataUsed.toFixed(1)} GB`;
  aggregated.thisWeek.dataUsed = `${aggregated.thisWeek.dataUsed.toFixed(1)} GB`;
  aggregated.thisMonth.dataUsed = `${aggregated.thisMonth.dataUsed.toFixed(1)} GB`;

  // Calculate averages
  const siteCount = segmentSiteIds.length;
  aggregated.thisWeek.avgDuration = `${Math.round(aggregated.thisWeek.uniqueVisitors / siteCount)}h`;
  aggregated.thisMonth.avgDuration = `${Math.round(aggregated.thisMonth.uniqueVisitors / siteCount)}h`;

  return aggregated;
};

