export const generateVoucherCode = (prefix = "GV") => {
  const chars = "ABCDEFGHJKLMNPQRSTUVWXYZ23456789";
  let code = prefix;
  for (let i = 0; i < 6; i++) {
    code += chars.charAt(Math.floor(Math.random() * chars.length));
  }
  return code;
};

// Get all unique sites for a segment from guest data
export const getSitesBySegment = (segment) => {
  if (!segment) return [];
  const sites = new Map();
  const segmentLabel = segment.charAt(0).toUpperCase() + segment.slice(1);
  guestUsers.filter(g => g.segment === segment).forEach(guest => {
    if (!sites.has(guest.siteId)) {
      // Create effectiveSiteName in format "Segment - SiteName"
      sites.set(guest.siteId, {
        siteId: guest.siteId,
        siteName: guest.siteName,
        location: guest.location,
        effectiveSiteName: `${segmentLabel} - ${guest.siteName}`,
      });
    }
  });
  return Array.from(sites.values());
};

